## Built-in math function

x = min(1, 2, 3)
y = max(1, 2, 3)

print(x)
print(y)

## abs() : absolute(positive)
z = abs(-11.99)
print(z)

# pow()
p = pow(3, 3)
print(p)

## The math module

import math

x = math.sqrt(64)
print(x)

# rounds a number upwards to its nearest integer
# rounds a number downwards to its nearest integer
c = math.ceil(1.7)
f = math.floor(1.7)

print(c)
print(f)

# PI

PI = math.pi 
print(PI)

